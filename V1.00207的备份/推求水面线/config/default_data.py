def get_default_nodes(start_station: float = 0.0):
    """
    获取默认节点数据，包含计算后的几何参数
    
    Args:
        start_station: 起始桩号（m），默认为0
    
    Returns:
        List[ChannelNode]: 默认节点数据列表
    """
    from models.data_models import ChannelNode, ProjectSettings
    from models.enums import StructureType, InOutType
    from core.geometry_calc import GeometryCalculator
    
    nodes = []
    for i, (flow_section, name, struct_type, in_out, x, y) in enumerate(DEFAULT_DATA):
        node = ChannelNode()
        node.flow_section = flow_section
        node.name = name
        node.ip_number = i
        node.x = x
        node.y = y
        node.turn_radius = DEFAULT_TURN_RADIUS  # 设置默认转弯半径
        
        # 设置结构形式
        if struct_type:
            try:
                node.structure_type = StructureType.from_string(struct_type)
            except ValueError:
                pass
        
        # 设置进出口
        if in_out == "进":
            node.in_out = InOutType.INLET
        elif in_out == "出":
            node.in_out = InOutType.OUTLET
        else:
            node.in_out = InOutType.NORMAL
        
        nodes.append(node)
    
    # 计算几何参数
    try:
        settings = ProjectSettings(turn_radius=DEFAULT_TURN_RADIUS, start_station=start_station)
        geo_calc = GeometryCalculator(settings)
        geo_calc.calculate_all_geometry(nodes)
        geo_calc.calculate_stations(nodes, start_station)
    except Exception as e:
        print(f"计算几何参数失败: {e}")
    
    return nodes
